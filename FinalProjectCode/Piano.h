// render keyboard model
void renderPiano(const GLuint& shader, int vao, GLuint textureID1, GLuint textureID2) {
    // keyboard
    // keyboard matrix
    mat4 dol = translate(mat4(1.0f), vec3(-12.0f, 2.0f, -12.0f)) * rotate(mat4(1.0f), radians(15.0f + rotateAmountDol), vec3(1.0f, 0.0f, 0.0f)) *
        scale(mat4(1.0f), vec3(1.0f, 0.4f, 4.0f));
    mat4 re = translate(mat4(1.0f), vec3(2.05f - 12.0f, 2.0f, -12.0f)) * rotate(mat4(1.0f), radians(15.0f + rotateAmountRe), vec3(1.0f, 0.0f, 0.0f)) *
        scale(mat4(1.0f), vec3(1.0f, 0.4f, 4.0f));
    mat4 mi = translate(mat4(1.0f), vec3(4.1f - 12.0f, 2.0f, -12.0f)) * rotate(mat4(1.0f), radians(15.0f + rotateAmountMi), vec3(1.0f, 0.0f, 0.0f)) *
        scale(mat4(1.0f), vec3(1.0f, 0.4f, 4.0f));
    mat4 fa = translate(mat4(1.0f), vec3(6.15f - 12.0f, 2.0f, -12.0f)) * rotate(mat4(1.0f), radians(15.0f + rotateAmountFa), vec3(1.0f, 0.0f, 0.0f)) *
        scale(mat4(1.0f), vec3(1.0f, 0.4f, 4.0f));
    mat4 so = translate(mat4(1.0f), vec3(8.2 - 12.0f, 2.0f, -12.0f)) * rotate(mat4(1.0f), radians(15.0f + rotateAmountSo), vec3(1.0f, 0.0f, 0.0f)) *
        scale(mat4(1.0f), vec3(1.0f, 0.4f, 4.0f));
    // active texture, bind vao
    glActiveTexture(GL_TEXTURE0);
    glBindVertexArray(vao);

    glBindTexture(GL_TEXTURE_2D, textureID1);
    SetUniformMat4(shader, "model_matrix", dol);
    glDrawArrays(GL_TRIANGLES, 0, 36);


    glBindTexture(GL_TEXTURE_2D, textureID2);
    SetUniformMat4(shader, "model_matrix", re);
    glDrawArrays(GL_TRIANGLES, 0, 36);

    glBindTexture(GL_TEXTURE_2D, textureID1);
    SetUniformMat4(shader, "model_matrix", mi);
    glDrawArrays(GL_TRIANGLES, 0, 36);


    glBindTexture(GL_TEXTURE_2D, textureID2);
    SetUniformMat4(shader, "model_matrix", fa);
    glDrawArrays(GL_TRIANGLES, 0, 36);


    glBindTexture(GL_TEXTURE_2D, textureID1);
    SetUniformMat4(shader, "model_matrix", so);
    glDrawArrays(GL_TRIANGLES, 0, 36);
    glBindVertexArray(0);
}

void renderSphere(const GLuint& shader, int vao) {
    // set matrix
    
    mat4 bodySphere = translate(mat4(1.0f), vec3(translateVariableX-11, translateVariableY-8, translateVariableZ-12))
        * rotate(mat4(1.0f), radians(rotateAmount), vec3(0.0f, 1.0f, 0.0f))
        * scale(mat4(1.0f), vec3(1.0f, 1.0f, 1.0f));

    // active texture, bind vao

    glBindVertexArray(vao);
    SetUniformMat4(shader, "model_matrix", bodySphere);
    glDrawArrays(GL_TRIANGLE_STRIP, 0, numOfVertices);
    glBindVertexArray(0);
}

void playPiano() {

    float hitPointY = 11.8f;
    // hit dol
    if (translateVariableY <= hitPointY && translateVariableX > -1.8f && translateVariableX < -0.1f) {
        rotateAmountDol = 5.0f;
        playSounddo();
    }
    // hit re
    if (translateVariableY <= hitPointY && translateVariableX > 0 && translateVariableX < 1.8f) {
        rotateAmountRe = 5.0f;
        playSoundre();
    }
    // hit mi
    if (translateVariableY <= hitPointY && translateVariableX > 1.8f && translateVariableX < 3.6f) {
        rotateAmountMi = 5.0f;
        playSoundmi();
    }
    // hit fa
    if (translateVariableY <= hitPointY && translateVariableX > 3.6f && translateVariableX < 5.4f) {
        rotateAmountFa = 5.0f;
        playSoundfa();
    }
    // hit so
    if (translateVariableY <= hitPointY && translateVariableX > 5.4f && translateVariableX < 7.2f) {
        rotateAmountSo = 5.0f;
        playSoundsol();
    }
    // key reset
    if (translateVariableY > 12.0f) {
        rotateAmountDol = 0.0f;
        rotateAmountRe = 0.0f;
        rotateAmountMi = 0.0f;
        rotateAmountFa = 0.0f;
        rotateAmountSo = 0.0f;
    }
}
